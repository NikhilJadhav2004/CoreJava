package com.seter_geter;

public class Studabt {
	private int id;
	private char div;
	private String name;
	private String address;
	private double mark;
	private int contact;

	public void setId(int sid) {
		id = sid;
	}

	public int getId() {
		return id;
	}

	public void setDiv(char sdiv) {
		div = sdiv;
	}

	public char getdiv() {
		return div;
	}

	public void setName(String sname) {
		name = sname;
	}

	public String getName() {
		return name;
	}

	public void setAddress(String saddr) {

		address = saddr;

	}

	public String getAddress() {
		return address;
	}

	public void setMark(double smark) {
		mark = smark;
	}

	public double getMark() {
		return mark;
	}

	public void setContact(int scontact) {
		contact = scontact;
	}

	public int getContact() {
		return contact;
	}
}
